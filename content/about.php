<h2>About Dr. Elia</h2>
<p>
Dr. Elia Gourgouris has been inspiring organizations and individuals 
through Executive coaching, Leadership development, Keynote 
speaking, Business consulting and Relationship coaching for more 
than 25 years. He's a nationally known speaker, relationship expert 
and President of LDS Executive Coaching. 
For almost 20 years, Dr. Gourgouris built a private practice focused upon improving 
relationships, overcoming addictions, and supporting marriages and families. For 
the last ten years, his focus has been primarily coaching and consulting with 
executives, organizations and businesses to help them achieve their highest level of
performance, thereby improving the bottom line.
</p>
