<p>
He holds a Bachelor of Arts in psychology from UCLA and his M.A. and Ph.D. in 
psychology from the California Graduate Institute. In addition to his executive and 
relationship coaching, Dr. Elia is a published author with more than 130 articles to 
his name, as well as several audio products. He also serves on the Board of Trustees
for Superior, Colorado, where he lives with his wife and two sons.
A great passion of Dr. Elia’s is to help individuals achieve their best self. To this end, 
he inspires people with keynote addresses, uplifting quotes, articles and other 
meaningful content at The Happiness Center (<a href="http://www.thehappinesscenter.com" target="_blank">TheHappinessCenter.com</a>).
